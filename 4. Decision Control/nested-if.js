let number = 10;
if (number > 0) {
    if (number % 2 === 0) {
        console.log("Number is positive and even");
    } else {
        console.log("Number is positive and odd");
    }
} else if (number < 0) {
    if (number % 2 === 0) {
        console.log("Number is negative and even");
    } else {
        console.log("Number is negative and odd");
    }
}
